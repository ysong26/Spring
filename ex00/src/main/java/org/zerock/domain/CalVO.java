package org.zerock.domain;

public class CalVO {
 int add;
 int sub;
 int mul;
 int divi;
 
public int getAdd() {
	return add;
}
public void setAdd(int add) {
	this.add = add;
}
public int getSub() {
	return sub;
}
public void setSub(int sub) {
	this.sub = sub;
}
public int getMul() {
	return mul;
}
public void setMul(int mul) {
	this.mul = mul;
}
public int getDivi() {
	return divi;
}
public void setDivi(int divi) {
	this.divi = divi;
}
 
}
